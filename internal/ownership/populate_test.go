func TestBuildPopulatePrompt_NoFrameworkProposedGlobs(t *testing.T) {
	t.Parallel()

	// ZFC negative assertion: the framework must NOT propose
	// `internal/<domain>/**` (the removed Req 13 heuristic) or any
	// pre-filled glob for the named domain.
	prompt := BuildPopulatePrompt("payments")

	banned := []string{
		"internal/payments/**",
		"internal/<domain>/**",
		"cmd/payments/**",
	}
	for _, b := range banned {
		if strings.Contains(prompt, b) {
			t.Errorf("populate prompt contains framework-proposed glob %q (ZFC violation)\n--- prompt ---\n%s", b, prompt)
		}
	}
}

// --- DomainsNeedingPopulate (Req 10 no-arg enumeration) ---

// writeDomain creates .mindspec/docs/domains/<name>/ with optional
// OWNERSHIP.yaml content (manifest omitted when content is "").
func writeDomain(t *testing.T, root, name, manifest string) {
	t.Helper()
	dir := filepath.Join(root, ".mindspec", "docs", "domains", name)
	if err := os.MkdirAll(dir, 0755); err != nil {
		t.Fatal(err)
	}
	if manifest != "" {
		if err := os.WriteFile(filepath.Join(dir, "OWNERSHIP.yaml"), []byte(manifest), 0644); err != nil {
			t.Fatal(err)
		}
	}
}

func TestDomainsNeedingPopulate_MissingAndEmptyStubOnly(t *testing.T) {
	t.Parallel()
	root := t.TempDir()

	writeDomain(t, root, "absent", "")                                           // missing manifest
	writeDomain(t, root, "stub", string(RenderStub("mindspec domain add stub"))) // empty stub
	writeDomain(t, root, "filled", "paths:\n  - internal/filled/**\n")           // populated

	got, err := DomainsNeedingPopulate(root)
	if err != nil {
		t.Fatalf("DomainsNeedingPopulate: %v", err)
	}
	want := []string{"absent", "stub"}
	if !reflect.DeepEqual(got, want) {
		t.Errorf("DomainsNeedingPopulate = %v, want %v", got, want)
	}
}

