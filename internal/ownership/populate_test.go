// --- RenderStub (Req 8) ---

func TestRenderStub_SubstitutesCommandAndKeepsEmptyBody(t *testing.T) {
	t.Parallel()

	stub := string(RenderStub("mindspec domain add payments"))

	if !strings.HasPrefix(stub, "# Auto-generated by mindspec domain add payments on ") {
		t.Errorf("stub first line missing <command> substitution:\n%s", stub)
	}
	if !strings.HasSuffix(stub, "paths: []\n") {
		t.Errorf("stub body must end with the empty `paths: []` stub, got:\n%s", stub)
	}
	// ZFC: the framework never pre-fills a real claim. The only glob in
	// the stub is the spec-verbatim hand-edit example inside the comment.
	for _, line := range strings.Split(stub, "\n") {
		if strings.HasPrefix(line, "#") || strings.TrimSpace(line) == "" {
			continue
		}
		if line != "paths: []" {
			t.Errorf("stub contains non-comment content beyond `paths: []`: %q", line)
		}
	}
}

func TestRenderStub_BodyIdenticalAcrossCommands(t *testing.T) {
	t.Parallel()

	a := string(RenderStub("mindspec doctor --fix"))
	b := string(RenderStub("mindspec domain add payments"))

	// Strip the variable first line (command + timestamp); everything
	// after must be byte-identical (Req 8: one helper, one variable).
	tailA := a[strings.Index(a, "\n"):]
	tailB := b[strings.Index(b, "\n"):]
	if tailA != tailB {
		t.Errorf("stub bodies differ beyond the first line:\n--- doctor ---\n%s\n--- domain add ---\n%s", tailA, tailB)
	}
}

