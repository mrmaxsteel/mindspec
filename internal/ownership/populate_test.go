// --- BuildSourcePopulatePrompt (Req 12) ---

func TestBuildSourcePopulatePrompt_RequiredContent(t *testing.T) {
	t.Parallel()

	prompt := BuildSourcePopulatePrompt()

	required := []string{
		"source_globs:",
		".mindspec/config.yaml",
		"The framework deliberately provides no pattern hints",
		"unclaimed-source",
		"missing-source-globs",
	}
	for _, r := range required {
		if !strings.Contains(prompt, r) {
			t.Errorf("source-populate prompt missing %q\n--- prompt ---\n%s", r, prompt)
		}
	}

	// The spec-verbatim text wraps "FULLY REPLACES" across a line
	// break; assert against a whitespace-normalized view so the
	// coverage warning is pinned regardless of wrapping.
	flat := strings.Join(strings.Fields(prompt), " ")
	if !strings.Contains(flat, "FULLY REPLACES") {
		t.Errorf("source-populate prompt missing the FULLY REPLACES coverage warning\n--- prompt ---\n%s", prompt)
	}
}

